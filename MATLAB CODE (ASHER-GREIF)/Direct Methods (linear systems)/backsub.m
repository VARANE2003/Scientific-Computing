function x = backsub(A,b)
% συνάρτηση x = backsub(A,b)
%
% Για μια άνω τριγωνική, μη ιδιάζουσα μήτρα Α
% διαστάσεων n επί n και ένα διάνυσμα b διάστασης n,
% επιστρέφει το διάνυσμα x που αποτελεί λύση της Ax = b

n = length(b); x = b;
x(n) = b(n) / A(n,n);
for k = n-1:-1:1
    x(k) = ( b(k) - A(k,k+1:n)*x(k+1:n) ) / A(k,k);
end


end